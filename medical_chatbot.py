# دیتابیس ساده بیماری‌ها (دیکشنری)
diseases = {
    "سرماخوردگی": "استراحت کن، مایعات گرم بخور و اگر بهتر نشدی به پزشک مراجعه کن.",
    "سردرد": "ممکنه به دلیل کم‌آبی یا خستگی باشه. کمی استراحت کن و آب بنوش.",
    "تب": "دمای بدنت بالاست. مایعات زیاد بخور و اگر ادامه داشت، دکتر برو."
}

print("سلام! من چت‌بات پزشکی هستم 🤖")
print("میتونی نام بیماری‌ات رو بگی تا راهنمایی کنم.")
print("اگر خواستی بیماری جدیدی اضافه کنی، بنویس: اضافه کردن")
print("برای خروج بنویس: خروج")
print("-" * 40)

while True:
    user_input = input("نام بیماری: ")

    if user_input == "خروج":
        print("خدانگهدار! 🌸 امیدوارم همیشه سالم باشی.")
        break

    elif user_input == "اضافه کردن":
        new_disease = input("نام بیماری جدید رو وارد کن: ")
        new_advice = input("توضیحات یا راهکار برای این بیماری: ")
        diseases[new_disease] = new_advice
        print("✅ بیماری با موفقیت اضافه شد.")
        print("-" * 40)

    elif user_input in diseases:
        print("پیشنهاد من: ", diseases[user_input])
        print("-" * 40)

    else:
        print("❌ متأسفم، در دیتابیس من اطلاعاتی درباره این بیماری وجود ندارد.")
        print("👉 می‌تونی با گزینه 'اضافه کردن' این بیماری رو ثبت کنی.")
        print("-" * 40)